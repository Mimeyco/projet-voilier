//MODAL APERO

const aperoModal = document.getElementById("aperoModal");
const aperoBtn = document.querySelectorAll(".aperoModalTrigger");
const aperospan = document.getElementById("apero-close");

aperoBtn[0].onclick = function () {
  aperoModal.style.display = "block";
};
aperoBtn[1].onclick = function () {
  aperoModal.style.display = "block";
};

aperospan.onclick = function () {
  aperoModal.style.display = "none";
};

window.onclick = function (event) {
  if (event.target == aperoModal) {
    aperoModal.style.display = "none";
  }
};
